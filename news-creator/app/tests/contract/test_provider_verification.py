def _get_free_port() -> int:
    """Get a free port from the OS."""
    with socket.socket(socket.AF_INET, socket.SOCK_STREAM) as s:
        s.bind(("127.0.0.1", 0))
        return s.getsockname()[1]


def _create_provider_app() -> FastAPI:
    """Create a FastAPI app with mocked dependencies for provider verification."""
    app = FastAPI()

    # --- Mock summarize usecase ---
    import news_creator.handler.summarize_handler as summarize_mod

    importlib.reload(summarize_mod)

    # Track provider state to switch mock behavior
    provider_state: dict[str, Any] = {"mode": "default"}

    mock_summarize_usecase = Mock()

    async def _dynamic_generate_summary(*args: Any, **kwargs: Any):
        if provider_state["mode"] == "queue_full":
            from news_creator.gateway.hybrid_priority_semaphore import QueueFullError

            raise QueueFullError("Queue depth 20 >= max 20")
        return (
            "これはテスト記事の要約です。",
            {
                "model": "gemma4-e4b-q4km",
                "prompt_tokens": 150,
                "completion_tokens": 80,
                "total_duration_ms": 2500.0,
            },
        )

    mock_summarize_usecase.generate_summary = AsyncMock(
        side_effect=_dynamic_generate_summary
    )
    router = summarize_mod.create_summarize_router(mock_summarize_usecase)
    app.include_router(router)

    # --- Mock chat handler ---
    import news_creator.handler.chat_handler as chat_mod

    importlib.reload(chat_mod)

    mock_ollama_gateway = Mock()

    # Non-streaming: gateway.chat_generate(payload=payload) -> dict
    async def _mock_chat_generate(payload: dict) -> Dict[str, Any]:
        return {
            "message": {"content": "This is a non-streaming response."},
            "done": True,
        }

    mock_ollama_gateway.chat_generate = AsyncMock(side_effect=_mock_chat_generate)

    # Streaming: gateway.chat_stream(payload=payload) -> async iterable of dict
    async def _mock_chat_stream_iter(payload: dict):
        chunks = [
            {"message": {"content": "Hello"}, "done": False},
            {"message": {"content": " world"}, "done": False},
            {"message": {"content": ""}, "done": True},
        ]
        for chunk in chunks:
            yield chunk

    # chat_stream is awaited then iterated, so return the async generator directly
    mock_ollama_gateway.chat_stream = AsyncMock(
        return_value=_mock_chat_stream_iter(payload={})
    )

    chat_router = chat_mod.create_chat_router(mock_ollama_gateway)
    app.include_router(chat_router)

    # --- Mock recap summary handler ---
    # Lightweight stub endpoints matching the production RecapSummaryResponse /
    # BatchRecapSummaryResponse pydantic shapes. recap-worker pacts expect:
    # - POST /v1/summary/generate         → { job_id, genre, summary: {title, bullets, language}, metadata: {model} }
    # - POST /v1/summary/generate/batch   → { responses: [<RecapSummaryResponse>], errors: [] }
    # The "the LLM queue is full" state makes /v1/summary/generate return a
    # 503 with a queue-full error body.
    @app.post("/v1/summary/generate")
    async def _stub_recap_summary(payload: dict[str, Any]) -> Any:
        from fastapi.responses import JSONResponse

        if provider_state["mode"] == "queue_full":
            # Mirror production: 429 + Retry-After on QueueFullError.
            return JSONResponse(
                status_code=429,
                content={"error": "queue full"},
                headers={"Retry-After": "30"},
            )
        return {
            "job_id": payload.get("job_id", "00000000-0000-0000-0000-000000000001"),
            "genre": payload.get("genre", "tech"),
            "summary": {
                "title": "テクノロジー週間要約",
                "bullets": ["AI関連の進展が報告された。"],
                "language": "ja",
            },
            "metadata": {"model": "gemma4-e4b-q4km"},
        }

    @app.post("/v1/summary/generate/batch")
    async def _stub_recap_summary_batch(payload: dict[str, Any]) -> Any:
        requests = payload.get("requests", [])
        responses = [
            {
                "job_id": req.get("job_id", "00000000-0000-0000-0000-000000000001"),
                "genre": req.get("genre", "tech"),
                "summary": {
                    "title": "Summary Title",
                    "bullets": ["Bullet 1"],
                    "language": "ja",
                },
                "metadata": {"model": "gemma4-e4b-q4km"},
            }
            for req in requests
        ]
        return {"responses": responses, "errors": []}

    # --- Mock plan-query handler ---
    import news_creator.handler.plan_query_handler as plan_query_mod

    importlib.reload(plan_query_mod)

    mock_plan_query_usecase = Mock()

    async def _mock_plan_query(request: Any) -> Any:
        from news_creator.domain.models import PlanQueryResponse, QueryPlan

        return PlanQueryResponse(
            plan=QueryPlan(
                reasoning="The user is asking about the causes of Iran's oil crisis. This requires causal analysis.",
                resolved_query=request.query,
                search_queries=[request.query, f"{request.query} 分析"],
                intent="causal_explanation",
                retrieval_policy="global_only",
                answer_format="causal_analysis",
                should_clarify=False,
                topic_entities=["test"],
            ),
            original_query=request.query,
            model="gemma4-e4b-12k",
            processing_time_ms=150.0,
        )

    mock_plan_query_usecase.plan_query = AsyncMock(side_effect=_mock_plan_query)

    plan_query_router = plan_query_mod.create_plan_query_router(mock_plan_query_usecase)
    app.include_router(plan_query_router)

    # --- Provider state registry ---
    async def _set_default(_params: dict[str, Any]) -> None:
        provider_state["mode"] = "default"

    async def _set_queue_full(_params: dict[str, Any]) -> None:
        provider_state["mode"] = "queue_full"

    registry: StateRegistry = {
        "the LLM model is loaded and ready": _set_default,
        "the LLM model is loaded and ready for chat": _set_default,
        "the LLM model is loaded and ready for query planning": _set_default,
        "the LLM queue is full": _set_queue_full,
        # acolyte-orchestrator's provider state for /api/v1/summarize replay.
        "news-creator is ready": _set_default,
    }

    @app.post("/_pact/provider-states")
    async def provider_states(payload: dict[str, Any]) -> dict:
        await dispatch(registry, payload)
        return {"status": "ok"}

    return app


@pytest.fixture(scope="module")
def provider_url():
    """Start the provider server on a free port for all tests in this module."""
    port = _get_free_port()
    app = _create_provider_app()

    config = uvicorn.Config(app, host="127.0.0.1", port=port, log_level="warning")
    server = uvicorn.Server(config)

    thread = threading.Thread(target=server.run, daemon=True)
    thread.start()

    # Wait for server to be ready
    base_url = f"http://127.0.0.1:{port}"
    for _ in range(100):
        try:
            urllib.request.urlopen(f"{base_url}/openapi.json")
            break
        except Exception:
            time.sleep(0.05)
    else:
        pytest.fail("Provider server did not start")

    yield base_url, port


def _configure_verifier(
    verifier: Verifier,
    base_url: str,
    *,
    consumer_name: str | None = None,
    local_pact_path: Path | None = None,
) -> None:
    """Configure verifier with Broker or local file source.

    When PACT_BROKER_BASE_URL is set, loads contracts from Broker.
    Otherwise falls back to local pact files.
    """
    if PACT_BROKER_URL:
        builder = verifier.broker_source(
            url=PACT_BROKER_URL,
            username=PACT_BROKER_USERNAME,
            password=PACT_BROKER_PASSWORD,
            selector=True,
        )
        # Pact's recommended provider configuration: include both the
        # main-branch pacts (new contracts) and the currently deployed or
        # released pacts. Without deployed_or_released, the broker never
        # records a verification against the consumer version currently
        # in production after a SHA bump, so can-i-deploy stays "unknown".
        if consumer_name:
            builder = builder.consumer_version(
                consumer=consumer_name, main_branch=True,
            )
            builder = builder.consumer_version(
                consumer=consumer_name, deployed_or_released=True,
            )
        else:
            builder = builder.consumer_version(main_branch=True)
            builder = builder.consumer_version(deployed_or_released=True)
        builder.build()

        if PACT_PROVIDER_VERSION:
            verifier.set_publish_options(
                version=PACT_PROVIDER_VERSION,
                url=PACT_BROKER_URL,
                branch=PACT_PROVIDER_BRANCH,
            )
    elif local_pact_path and local_pact_path.exists():
        verifier.add_source(str(local_pact_path))
    else:
        pytest.skip(
            f"No Broker URL set and pact file not found: {local_pact_path}. "
            "Set PACT_BROKER_BASE_URL or run Go consumer tests first."
        )

    verifier.state_handler(
        f"{base_url}/_pact/provider-states",
        teardown=False,
        body=True,
    )


@pytest.mark.skipif(
    not PACT_BROKER_URL and not PRE_PROCESSOR_PACT.exists(),
    reason=f"No Broker URL and pact file not found: {PRE_PROCESSOR_PACT}",
)
def test_verify_pre_processor_contract(provider_url: tuple[str, int]):
    """Verify news-creator satisfies pre-processor's contract."""
    base_url, port = provider_url

    verifier = Verifier("news-creator")
    verifier.add_transport(protocol="http", port=port)
    _configure_verifier(
        verifier,
        base_url,
        consumer_name="pre-processor",
        local_pact_path=PRE_PROCESSOR_PACT,
    )

    verifier.verify()


