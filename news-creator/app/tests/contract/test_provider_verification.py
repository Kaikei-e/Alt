"""Provider verification tests for news-creator.

Verifies that news-creator fulfills the contracts defined by its consumers:
- pre-processor (via /api/v1/summarize)
- rag-orchestrator (via /api/chat)

These tests start a real FastAPI app with mocked usecases/gateways
and verify it against the pact files generated by consumer tests.
"""

import importlib
import json
import logging
import socket
import threading
import time
import urllib.request
from pathlib import Path
from typing import Any, Dict, Optional
from unittest.mock import AsyncMock, Mock

import pytest
import uvicorn
from fastapi import FastAPI
from pact import Verifier

logger = logging.getLogger(__name__)

# Pact files generated by Go consumer tests
PACT_DIR = Path(__file__).resolve().parent.parent.parent.parent.parent / "pacts"
PRE_PROCESSOR_PACT = PACT_DIR / "pre-processor-news-creator.json"
RAG_ORCHESTRATOR_PACT = (
    Path(__file__).resolve().parent.parent.parent.parent.parent
    / "rag-orchestrator"
    / "pacts"
    / "rag-orchestrator-news-creator.json"
)


def _get_free_port() -> int:
    """Get a free port from the OS."""
    with socket.socket(socket.AF_INET, socket.SOCK_STREAM) as s:
        s.bind(("127.0.0.1", 0))
        return s.getsockname()[1]


def _create_provider_app() -> FastAPI:
    """Create a FastAPI app with mocked dependencies for provider verification."""
    app = FastAPI()

    # --- Mock summarize usecase ---
    import news_creator.handler.summarize_handler as summarize_mod

    importlib.reload(summarize_mod)

    # Track provider state to switch mock behavior
    provider_state = {"current": "default"}

    mock_summarize_usecase = Mock()

    async def _dynamic_generate_summary(*args: Any, **kwargs: Any):
        if provider_state["current"] == "queue_full":
            from news_creator.gateway.hybrid_priority_semaphore import QueueFullError
            raise QueueFullError("Queue depth 20 >= max 20")
        return (
            "これはテスト記事の要約です。",
            {
                "model": "gemma3:4b-it-qat",
                "prompt_tokens": 150,
                "completion_tokens": 80,
                "total_duration_ms": 2500.0,
            },
        )

    mock_summarize_usecase.generate_summary = AsyncMock(side_effect=_dynamic_generate_summary)
    router = summarize_mod.create_summarize_router(mock_summarize_usecase)
    app.include_router(router)

    # --- Mock chat handler ---
    import news_creator.handler.chat_handler as chat_mod

    importlib.reload(chat_mod)

    mock_ollama_gateway = Mock()

    # Non-streaming: gateway.chat_generate(payload=payload) -> dict
    async def _mock_chat_generate(payload: dict) -> Dict[str, Any]:
        return {
            "message": {"content": "This is a non-streaming response."},
            "done": True,
        }

    mock_ollama_gateway.chat_generate = AsyncMock(side_effect=_mock_chat_generate)

    # Streaming: gateway.chat_stream(payload=payload) -> async iterable of dict
    async def _mock_chat_stream_iter(payload: dict):
        chunks = [
            {"message": {"content": "Hello"}, "done": False},
            {"message": {"content": " world"}, "done": False},
            {"message": {"content": ""}, "done": True},
        ]
        for chunk in chunks:
            yield chunk

    # chat_stream is awaited then iterated, so return the async generator directly
    mock_ollama_gateway.chat_stream = AsyncMock(return_value=_mock_chat_stream_iter(payload={}))

    chat_router = chat_mod.create_chat_router(mock_ollama_gateway)
    app.include_router(chat_router)

    # --- Provider state handler ---
    from pydantic import BaseModel

    class ProviderStateRequest(BaseModel):
        state: str = ""
        action: str = "setup"

    @app.post("/_pact/provider-states")
    async def provider_states(req: ProviderStateRequest) -> dict:
        """Handle provider state setup for Pact verification."""
        logger.info("Setting up provider state: %s (action=%s)", req.state, req.action)
        if "queue is full" in req.state.lower():
            provider_state["current"] = "queue_full"
        else:
            provider_state["current"] = "default"
        return {"status": "ok"}

    return app


@pytest.fixture(scope="module")
def provider_url():
    """Start the provider server on a free port for all tests in this module."""
    port = _get_free_port()
    app = _create_provider_app()

    config = uvicorn.Config(app, host="127.0.0.1", port=port, log_level="warning")
    server = uvicorn.Server(config)

    thread = threading.Thread(target=server.run, daemon=True)
    thread.start()

    # Wait for server to be ready
    base_url = f"http://127.0.0.1:{port}"
    for _ in range(100):
        try:
            urllib.request.urlopen(f"{base_url}/openapi.json")
            break
        except Exception:
            time.sleep(0.05)
    else:
        pytest.fail("Provider server did not start")

    yield base_url, port


@pytest.mark.skipif(
    not PRE_PROCESSOR_PACT.exists(),
    reason=f"Pact file not found: {PRE_PROCESSOR_PACT}. Run Go consumer tests first.",
)
def test_verify_pre_processor_contract(provider_url: tuple[str, int]):
    """Verify news-creator satisfies pre-processor's contract."""
    base_url, port = provider_url

    verifier = Verifier("news-creator")
    verifier.add_transport(protocol="http", port=port)
    verifier.add_source(str(PRE_PROCESSOR_PACT))
    verifier.state_handler(
        f"{base_url}/_pact/provider-states",
        teardown=False,
        body=True,
    )

    verifier.verify()


