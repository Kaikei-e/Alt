#!/bin/bash
# run-sv-connect-composite-test.sh
#
# 10-minute composite Connect-RPC load test for the production SV frontend path.
# It exercises /sv/api/v2 through nginx/SvelteKit, while external HTTP is served
# only by the mock RSS server.

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_ROOT="$(cd "$SCRIPT_DIR/../.." && pwd)"
cd "$PROJECT_ROOT"

USER_COUNT="${USER_COUNT:-200}"
SESSION_RATE_BASE="${SESSION_RATE_BASE:-2}"
SESSION_RATE_TARGET="${SESSION_RATE_TARGET:-5}"
SESSION_RATE_PEAK="${SESSION_RATE_PEAK:-8}"
PRE_ALLOCATED_VUS="${PRE_ALLOCATED_VUS:-32}"
MAX_VUS="${MAX_VUS:-128}"
MOCK_RSS_DELAY_MS="${MOCK_RSS_DELAY_MS:-150}"
MOCK_ARTICLE_DELAY_MS="${MOCK_ARTICLE_DELAY_MS:-350}"
MOCK_DELAY_JITTER_MS="${MOCK_DELAY_JITTER_MS:-75}"
PPROF_ENABLED="${PPROF_ENABLED:-true}"

GENERATED_OVERLAY="compose/sv-connect-load-test-generated.yaml"
K6_SCENARIO="/scripts/scenarios/sv-connect-composite-10m.js"
COMPOSE_ARGS=(
  --profile load-test
  --profile perf
  -f compose/compose.yaml
  -f compose/load-test.yaml
  -f "$GENERATED_OVERLAY"
  -p alt
)

cleanup() {
  set +e
  if [ -f "$GENERATED_OVERLAY" ]; then
    rm -f "$GENERATED_OVERLAY"
  fi
}
trap cleanup EXIT

cat > "$GENERATED_OVERLAY" <<OVERLAY_EOF
# Generated by run-sv-connect-composite-test.sh — DO NOT EDIT
services:
  mock-rss-server:
    environment:
      - MOCK_RSS_DELAY_MS=${MOCK_RSS_DELAY_MS}
      - MOCK_ARTICLE_DELAY_MS=${MOCK_ARTICLE_DELAY_MS}
      - MOCK_DELAY_JITTER_MS=${MOCK_DELAY_JITTER_MS}
    ports: !reset []
    deploy:
      replicas: 3
    networks:
      alt-network:
        aliases:
          - mock-rss-001

  alt-backend:
    environment:
      - FEED_ALLOWED_HOSTS=mock-rss-001
      - DOS_PROTECTION_RATE_LIMIT=10000
      - DOS_PROTECTION_BURST_LIMIT=20000
      - RATE_LIMIT_EXTERNAL_API_INTERVAL=10ms
      - RATE_LIMIT_EXTERNAL_API_BURST=500
      - RATE_LIMIT_EXTERNAL_API_UNSAFE_FAST=true
      - PPROF_ENABLED=${PPROF_ENABLED}
    ports:
      - "6060:6060"

  auth-hub:
    environment:
      - VALIDATE_RATE_LIMIT=1000
      - SESSION_RATE_LIMIT=1000

  kratos:
    ports:
      - "4434:4434"

  nginx:
    volumes:
      - ../alt-perf/k6/config/nginx-loadtest-10k.conf:/etc/nginx/nginx.conf:ro
      - ../alt-perf/k6/config/nginx-loadtest-realip.conf:/etc/nginx/conf.d/realip.conf:ro
    deploy:
      resources:
        limits:
          memory: 1G

  k6:
    deploy:
      resources:
        limits:
          memory: 16G
          cpus: '8'
OVERLAY_EOF

echo "========================================"
echo "  SV Connect-RPC Composite 10m Load Test"
echo "  Users: $USER_COUNT"
echo "  Session rate: ${SESSION_RATE_BASE} -> ${SESSION_RATE_TARGET} -> ${SESSION_RATE_PEAK} sessions/s"
echo "  VUs: pre=${PRE_ALLOCATED_VUS}, max=${MAX_VUS}"
echo "  Mock delays: rss=${MOCK_RSS_DELAY_MS}ms article=${MOCK_ARTICLE_DELAY_MS}ms jitter=${MOCK_DELAY_JITTER_MS}ms"
echo "  pprof: ${PPROF_ENABLED}"
echo "  nginx: worker_connections=8192 (loadtest-10k)"
echo "  RATE_LIMIT_EXTERNAL_API: interval=10ms burst=500"
echo "  mock-rss-server: 3 replicas"
echo "  DOS_PROTECTION: rate=10000 burst=20000"
echo "========================================"
echo

echo "Phase 1: starting required services..."
docker compose "${COMPOSE_ARGS[@]}" up --build --force-recreate -d mock-rss-server alt-backend alt-frontend-sv nginx auth-hub kratos

echo "Phase 2: verifying mock RSS server health..."
for _ in $(seq 1 20); do
  if docker compose "${COMPOSE_ARGS[@]}" exec -T mock-rss-server wget --spider -q http://localhost:8080/health 2>/dev/null; then
    break
  fi
  sleep 2
done
docker compose "${COMPOSE_ARGS[@]}" exec -T mock-rss-server wget --spider -q http://localhost:8080/health

echo "Phase 3: creating Kratos load-test users..."
deno run --allow-net --allow-write --allow-read --allow-env \
  alt-perf/scripts/feed-load-test-setup.ts \
  --count="${USER_COUNT}"

echo "Phase 4: running k6 scenario..."
K6_EXIT=0
docker compose "${COMPOSE_ARGS[@]}" run --rm \
  -e USERS_FILE=/scripts/data/load-test-users.json \
  -e K6_APP_BASE_URL=http://nginx/sv/api/v2 \
  -e MOCK_RSS_BASE_URL=http://mock-rss-001:8080 \
  -e SESSION_RATE_BASE="${SESSION_RATE_BASE}" \
  -e SESSION_RATE_TARGET="${SESSION_RATE_TARGET}" \
  -e SESSION_RATE_PEAK="${SESSION_RATE_PEAK}" \
  -e PRE_ALLOCATED_VUS="${PRE_ALLOCATED_VUS}" \
  -e MAX_VUS="${MAX_VUS}" \
  k6 run "${K6_SCENARIO}" || K6_EXIT=$?

if [ "${PPROF_ENABLED}" = "true" ]; then
  echo "  Collecting pprof profiles..."
  curl -sf http://localhost:6060/debug/pprof/heap > /tmp/pprof-heap-sv-composite.pb.gz 2>/dev/null || true
  curl -sf http://localhost:6060/debug/pprof/allocs > /tmp/pprof-allocs-sv-composite.pb.gz 2>/dev/null || true
  curl -sf http://localhost:6060/debug/pprof/goroutine > /tmp/pprof-goroutine-sv-composite.pb.gz 2>/dev/null || true
  echo "  pprof profiles saved to /tmp/pprof-*-sv-composite.pb.gz"
fi

echo "Phase 5: tearing down generated users and mock data..."
deno run --allow-net --allow-read --allow-write --allow-env --allow-run \
  alt-perf/scripts/feed-load-test-teardown.ts

echo
echo "Reports: alt-perf/reports/k6-*.json"

if [ "$K6_EXIT" -ne 0 ]; then
  echo "k6 exited with code ${K6_EXIT} (99 = thresholds crossed)"
  exit "$K6_EXIT"
fi
